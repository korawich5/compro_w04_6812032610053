#include <stdio.h>

int main() {
    char nameInitial = 'K'; // Character types
    int age = 18;  // Integer types
    float weight = 75.5;  // Floating point
    char gender[] = "Male"; // Character types

    printf("nameInitial = %c\n", nameInitial);
    printf("age = %d\n", age);
    printf("weight = %.2f\n", weight);
    printf("gender = %s\n", gender);
    
    return 0;
}